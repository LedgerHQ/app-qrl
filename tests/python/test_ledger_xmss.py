def ledger_send(cmd, params=None):
    if params is None:
        params = []
    answer = None

    start = time.time()
    try:
        cmd_str = "80{0:02x}".format(cmd)
        for p in params:
            cmd_str = cmd_str + "{0:02x}".format(p)

        answer = dongle.exchange(binascii.unhexlify(cmd_str))
    except CommException as e:
        print("COMMEXC: ", e)
    except Exception as e:
        print("COMMEXC: ", e)
    end = time.time()
    print(end - start)
    return answer


